import matplotlib.pyplot as plt
from matplotlib_venn import venn2



def venn_plot(list_A, list_B):
    # Define two lists
    # Compute the number of indices where the labels match (intersection) and differ
    intersection = sum(1 for i in range(len(list_A)) if list_A[i] == list_B[i])  # Matching indices
    only_A = sum(1 for i in range(len(list_A)) if list_A[i] != list_B[i])  # Non-matching indices in A
    only_B = sum(1 for i in range(len(list_B)) if list_B[i] != list_A[i])  # Non-matching indices in B

    # Create Venn Diagram
    plt.figure(figsize=(5, 5))
    venn2(subsets=(only_A, only_B, intersection))

    # Title
    # plt.title("Equal")

    # Show Plot
    plt.show()


